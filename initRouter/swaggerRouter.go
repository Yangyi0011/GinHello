package initRouter

import (
	"github.com/swaggo/files"       // swagger embed files
	"github.com/swaggo/gin-swagger" // gin-swagger middleware

	// swagger 文档生成在本项目中的目录，必须导入这个目录文档才能正常显示
	_ "GinHello/docs" // docs is generated by Swag CLI, you have to import it.
)

